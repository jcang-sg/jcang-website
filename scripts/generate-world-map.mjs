// Build-time generator for the decorative world-map watermark.
//
// Source data: Natural Earth countries-110m (public domain) via the
// `world-atlas` TopoJSON package. We project it once with d3-geo
// (geoNaturalEarth1, rotated to centre on Asia) and emit static SVG path
// strings. The app imports only these strings, so no map library or data
// ships to the client.
//
// Regenerate with: npm run generate:map

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { geoNaturalEarth1, geoPath, geoGraticule } from "d3-geo";
import { mesh } from "topojson-client";

const root = fileURLToPath(new URL("..", import.meta.url));

const topology = JSON.parse(
  readFileSync(root + "node_modules/world-atlas/countries-110m.json", "utf8"),
);

// Natural Earth has a ~2:1 aspect ratio.
const WIDTH = 1000;
const HEIGHT = 500;

// geoNaturalEarth1 centred on Asia: central meridian ~105°E -> rotate -105°.
const projection = geoNaturalEarth1().rotate([-105, 0]);
projection.fitExtent(
  [
    [0, 0],
    [WIDTH, HEIGHT],
  ],
  { type: "Sphere" },
);

const path = geoPath(projection);

// All country boundaries (coastlines + shared borders) as a thin mesh.
const borders = mesh(topology, topology.objects.countries);
// Faint 15° graticule.
const graticule = geoGraticule().step([15, 15])();

const bordersPath = path(borders);
const graticulePath = path(graticule);
const spherePath = path({ type: "Sphere" });

const out = `// AUTO-GENERATED by scripts/generate-world-map.mjs — do not edit by hand.
// Source: Natural Earth countries-110m (public domain) via world-atlas.
// Projection: d3-geo geoNaturalEarth1, rotated to centre on Asia (rotate -105°).
export const MAP_VIEWBOX = "0 0 ${WIDTH} ${HEIGHT}";
export const SPHERE_PATH = ${JSON.stringify(spherePath)};
export const GRATICULE_PATH = ${JSON.stringify(graticulePath)};
export const BORDERS_PATH = ${JSON.stringify(bordersPath)};
`;

mkdirSync(root + "src/lib", { recursive: true });
writeFileSync(root + "src/lib/world-map-data.ts", out);

console.log(
  `Wrote src/lib/world-map-data.ts (borders ${bordersPath.length}, graticule ${graticulePath.length} chars)`,
);
